public class Aritmatika {
    public static void main(String[] args) {
        
        int a = 200;
        int b = 10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        // Augmented Assigment

        int c =3;
        c+=2;
        System.out.println(c);
        c-=2;
        System.out.println(c);
        c*=2;
        System.out.println(c);
        c++;
        System.out.println(c);
        c--;
        System.out.println(c);

        Boolean result = true;
        System.out.println(!result);





    }
}
